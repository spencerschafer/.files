#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char	*dest2;
	unsigned char	*src2;

	dest2 = (unsigned char *)dest;
	src2 = (unsigned char *)src;
	while (n--)
	{
		*(dest2++) = *(src2);
		if (*(src2++) == (unsigned char)c)
			return (dest2);
	}
	return (NULL);
}
