#include "libft.h"

int	ft_atoi(const char *nptr)
{
	size_t		res;
	size_t		i;

	res = 0;
	i = 0;
	while ((nptr[i] >= '0') && (nptr[i] <= '9'))
	{
		res *= 10;
		res += (nptr[i++] - '0');
	}
	return (res);
}
