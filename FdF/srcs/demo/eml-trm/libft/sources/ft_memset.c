#include <stdlib.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	tmp = (unsigned char *)b;
	while (len--)
	{
		tmp[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
