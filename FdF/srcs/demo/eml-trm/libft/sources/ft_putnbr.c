#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	char	c;

	c = 45;
	if (n == (-2147483648))
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(1, &c, 1);
			n *= -1;
		}
		if (n <= 9)
			ft_putchar(n + 48);
		else
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
	}
}
